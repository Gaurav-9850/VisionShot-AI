class ImageQuality {

  final double brightness;

  final double contrast;

  final double blur;

  final double sharpness;

  final bool isDark;

  final bool isBlurry;

  ImageQuality({

    required this.brightness,

    required this.contrast,

    required this.blur,

    required this.sharpness,

    required this.isDark,

    required this.isBlurry,

  });

}